// Inicioni efektet e animacioneve
document.addEventListener("DOMContentLoaded", function () {
    // Inicioni AOS (Animate on Scroll)
    AOS.init({
        duration: 1000, // Kohëzgjatja e animacionit në milisekonda
        easing: "ease-in-out", // Stil i animacionit
        once: true, // Animacioni luhet vetëm një herë kur kalon në ekran
    });
});
